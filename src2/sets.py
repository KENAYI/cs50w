# Create an empty set
s = set()

# Add elements to set
s.add(1)
s.add(3)
s.add(5)
s.add(3)
s.add(4)

# Print the set
print(s)

# Find the size of the set
print(f"The set has {len(s)} elements.")

# Remove element from set
s.remove(5)
print(s)

# Find the size of the set
print(f"The set now has {len(s)} elements.")